#this is a program to print values of different variables having different data types using .format function


name = 'keshav'
a = "my name is {}".format(name)             
age = 20
b = "i am {} years old".format(age)
state = 'haryana'
c = "i live in {}".format(state)
print(a)
print(b)
print(c)


